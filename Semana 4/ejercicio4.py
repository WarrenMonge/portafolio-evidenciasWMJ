dia = input("Ingrese el día de la semana (por ejemplo, Lunes, Martes, Miércoles, Jueves): ")


if dia == "Lunes":
    print("Para el Lunes la dieta es: Avena y frutas tropicales")
elif dia == "Martes":
    print("Para el Martes la dieta es: Yogurt y frutas secas")
elif dia == "Miércoles":
    print("Para el Miércoles la dieta es: Café y galletas saladas")
elif dia == "Jueves":
    print("Para el Jueves la dieta es: Barra energética y jugo de naranja")
else:
    print("El día ingresado no es válido. Por favor, ingrese un día de la semana válido.")